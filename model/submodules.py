class WindowAttention_eef(nn.Module):
    r""" Window based multi-head self attention (W-MSA) module with relative position bias.
    It supports both of shifted and non-shifted window.

    Args:
        dim (int): Number of input channels.
        window_size (tuple[int]): The height and width of the window.
        num_heads (int): Number of attention heads.
        qkv_bias (bool, optional):  If True, add a learnable bias to query, key, value. Default: True
        qk_scale (float | None, optional): Override default qk scale of head_dim ** -0.5 if set
        attn_drop (float, optional): Dropout ratio of attention weight. Default: 0.0
        proj_drop (float, optional): Dropout ratio of output. Default: 0.0
    """

    def __init__(self, dim, window_size, num_heads, qkv_bias=True, qk_scale=None, attn_drop=0., proj_drop=0.):

        super().__init__()
        self.dim = dim
        self.window_size = window_size  # Wh, Ww
        self.num_heads = num_heads
        head_dim = dim // num_heads
        self.scale = qk_scale or head_dim ** -0.5

        # define a parameter table of relative position bias
        self.relative_position_bias_table = nn.Parameter(
            torch.zeros((2 * window_size[0] - 1) * (2 * window_size[1] - 1), num_heads))  # 2*Wh-1 * 2*Ww-1, nH


        # get pair-wise relative position index for each token inside the window
        coords_h = torch.arange(self.window_size[0])
        coords_w = torch.arange(self.window_size[1])
        coords = torch.stack(torch.meshgrid([coords_h, coords_w]))  # 2, Wh, Ww
        coords_flatten = torch.flatten(coords, 1)  # 2, Wh*Ww
        relative_coords = coords_flatten[:, :, None] - coords_flatten[:, None, :]  # 2, Wh*Ww, Wh*Ww
        relative_coords = relative_coords.permute(1, 2, 0).contiguous()  # Wh*Ww, Wh*Ww, 2
        relative_coords[:, :, 0] += self.window_size[0] - 1  # shift to start from 0
        relative_coords[:, :, 1] += self.window_size[1] - 1
        relative_coords[:, :, 0] *= 2 * self.window_size[1] - 1
        relative_position_index = relative_coords.sum(-1)  # Wh*Ww, Wh*Ww
        self.register_buffer("relative_position_index", relative_position_index)

        #self.qkv = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.qkv_eoa = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.qkv_eaa = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.qkv_f = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.attn_drop = nn.Dropout(attn_drop)

        # self.proj = nn.Linear(dim, dim)
        # self.proj_drop = nn.Dropout(proj_drop)

        self.proj_eoa = nn.Linear(dim, dim)
        self.proj_drop_eoa = nn.Dropout(proj_drop)

        self.proj_eaa = nn.Linear(dim, dim)
        self.proj_drop_eaa = nn.Dropout(proj_drop)

        self.proj_f = nn.Linear(dim, dim)
        self.proj_drop_f = nn.Dropout(proj_drop)

        trunc_normal_(self.relative_position_bias_table, std=.02)
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x,y,z, mask=None): #x:eoa y:frame z:eaa
        """
        Args:
            x: input features with shape of (num_windows*B, N, C)
            mask: (0/-inf) mask with shape of (num_windows, Wh*Ww, Wh*Ww) or None
        """
        B_, N, C = x.shape
        #print(x.size())
        # qkv = self.qkv(x).reshape(B_, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        # q, k, v = qkv[0], qkv[1], qkv[2]  # make torchscript happy (cannot use tensor as tuple)

        qkv_eoa = self.qkv_eoa(x).reshape(B_, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q_eoa, k_eoa, v_eoa = qkv_eoa[0], qkv_eoa[1], qkv_eoa[2]  # make torchscript happy (cannot use tensor as tuple)
        #print(q_eoa.size())
        #print(k_eoa.size())
        qkv_f = self.qkv_f(y).reshape(B_, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q_f, k_f, v_f = qkv_f[0], qkv_f[1], qkv_f[2]  # make torchscript happy (cannot use tensor as tuple)

        qkv_eaa = self.qkv_eaa(z).reshape(B_, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q_eaa, k_eaa, v_eaa = qkv_eaa[0], qkv_eaa[1], qkv_eaa[2]  # make torchscript happy (cannot use tensor as tuple)

        q_eoa = q_eoa * self.scale
        q_f = q_f * self.scale
        q_eaa = q_eaa * self.scale

        attn_eoa = (q_eoa @ k_eoa.transpose(-2, -1))
        attn_f = (q_f @ k_f.transpose(-2, -1))
        attn_eaa = (q_eaa @ k_eaa.transpose(-2, -1))


        relative_position_bias = self.relative_position_bias_table[self.relative_position_index.view(-1)].view(
            self.window_size[0] * self.window_size[1], self.window_size[0] * self.window_size[1], -1)  # Wh*Ww,Wh*Ww,nH
        relative_position_bias = relative_position_bias.permute(2, 0, 1).contiguous()  # nH, Wh*Ww, Wh*Ww

        attn_eoa = attn_eoa + relative_position_bias.unsqueeze(0)
        attn_f = attn_f + relative_position_bias.unsqueeze(0)
        attn_eaa = attn_eaa + relative_position_bias.unsqueeze(0)

        if mask is not None:
            nW = mask.shape[0]
            attn_eoa = attn_eoa.view(B_ // nW, nW, self.num_heads, N, N) + mask.unsqueeze(1).unsqueeze(0)
            attn_eoa = attn_eoa.view(-1, self.num_heads, N, N)
            attn_eoa = self.softmax(attn_eoa)

            attn_f = attn_f.view(B_ // nW, nW, self.num_heads, N, N) + mask.unsqueeze(1).unsqueeze(0)
            attn_f = attn_f.view(-1, self.num_heads, N, N)
            attn_f = self.softmax(attn_f)

            attn_eaa = attn_eaa.view(B_ // nW, nW, self.num_heads, N, N) + mask.unsqueeze(1).unsqueeze(0)
            attn_eaa = attn_eaa.view(-1, self.num_heads, N, N)
            attn_eaa = self.softmax(attn_eaa)
        else:
            attn_eoa = self.softmax(attn_eoa)
            attn_eaa = self.softmax(attn_eaa)
            attn_f = self.softmax(attn_f)

        #attn = self.attn_drop(attn)

        attn_all = attn_eoa + attn_eaa + attn_f

        #print(attn_all.size())
        out_x = (attn_all @ v_eoa).transpose(1, 2).reshape(B_, N, C)
        out_y = (attn_all @ v_f).transpose(1, 2).reshape(B_, N, C)
        out_z = (attn_all @ v_eaa).transpose(1, 2).reshape(B_, N, C)

        out_x = self.proj_eaa(out_x)
        out_x = self.proj_drop_eaa(out_x)

        out_y = self.proj_f(out_y)
        out_y = self.proj_drop_f(out_y)

        out_z = self.proj_eoa(out_z)
        out_z = self.proj_drop_eoa(out_z)
        return out_x, out_y, out_z
class FusionSwinTransformerBlock(nn.Module):
    def __init__(self, dim, input_resolution, num_heads, window_size=7, shift_size=0,
                 mlp_ratio=4., qkv_bias=True, qk_scale=None, drop=0., attn_drop=0., drop_path=0.,
                 act_layer=nn.GELU, norm_layer=nn.LayerNorm):
        super().__init__()
        self.dim = dim
        self.input_resolution = input_resolution
        self.num_heads = num_heads
        self.window_size = window_size
        self.shift_size = shift_size
        self.mlp_ratio = mlp_ratio
        if min(self.input_resolution) <= self.window_size:
            # if window size is larger than input resolution, we don't partition windows
            self.shift_size = 0
            self.window_size = min(self.input_resolution)
        assert 0 <= self.shift_size < self.window_size, "shift_size must in 0-window_size"

        if self.shift_size > 0:
            attn_mask = self.calculate_mask(self.input_resolution)
        else:
            attn_mask = None

        self.register_buffer("attn_mask", attn_mask)

        ##########
        self.norm1_eoa = norm_layer(dim)
        self.norm1_eaa = norm_layer(dim)
        self.norm1_f = norm_layer(dim)
        self.attn = WindowAttention_eef(
            dim, window_size=to_2tuple(self.window_size), num_heads=num_heads,
            qkv_bias=qkv_bias, qk_scale=qk_scale, attn_drop=attn_drop, proj_drop=drop)

        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()

        self.norm2_eoa = norm_layer(dim)
        self.mlp_eoa = Mlp(in_features=dim, hidden_features=int(dim * mlp_ratio), act_layer=act_layer, drop=drop)
        self.norm2_eaa = norm_layer(dim)
        self.mlp_eaa = Mlp(in_features=dim, hidden_features=int(dim * mlp_ratio), act_layer=act_layer, drop=drop)
        self.norm2_f = norm_layer(dim)
        self.mlp_f = Mlp(in_features=dim, hidden_features=int(dim * mlp_ratio), act_layer=act_layer, drop=drop)

    def calculate_mask(self, x_size):
        # calculate attention mask for SW-MSA
        H, W = x_size
        img_mask = torch.zeros((1, H, W, 1))  # 1 H W 1
        h_slices = (slice(0, -self.window_size),
                    slice(-self.window_size, -self.shift_size),
                    slice(-self.shift_size, None))
        w_slices = (slice(0, -self.window_size),
                    slice(-self.window_size, -self.shift_size),
                    slice(-self.shift_size, None))
        cnt = 0
        for h in h_slices:
            for w in w_slices:
                img_mask[:, h, w, :] = cnt
                cnt += 1

        mask_windows = window_partition(img_mask, self.window_size)  # nW, window_size, window_size, 1
        mask_windows = mask_windows.view(-1, self.window_size * self.window_size)
        attn_mask = mask_windows.unsqueeze(1) - mask_windows.unsqueeze(2)
        attn_mask = attn_mask.masked_fill(attn_mask != 0, float(-100.0)).masked_fill(attn_mask == 0, float(0.0))

        return attn_mask

    def forward(self, x, y , z , x_size):
        H, W = x_size
        B, L, C = x.shape
        # assert L == H * W, "input feature has wrong size"

        x_shortcut = x
        y_shortcut = y
        z_shortcut = z

        x = self.norm1_eoa(x)
        x = x.view(B, H, W, C)
        y = self.norm1_f(y)
        y = y.view(B, H, W, C)
        z = self.norm1_eaa(z)
        z = z.view(B, H, W, C)

        # cyclic shift
        if self.shift_size > 0:
            shifted_x = torch.roll(x, shifts=(-self.shift_size, -self.shift_size), dims=(1, 2))
            shifted_y = torch.roll(y, shifts=(-self.shift_size, -self.shift_size), dims=(1, 2))
            shifted_z = torch.roll(z, shifts=(-self.shift_size, -self.shift_size), dims=(1, 2))
        else:
            shifted_x = x
            shifted_y = y
            shifted_z = z

        # partition windows
        x_windows = window_partition(shifted_x, self.window_size)  # nW*B, window_size, window_size, C
        x_windows = x_windows.view(-1, self.window_size * self.window_size, C)  # nW*B, window_size*window_size, C
        y_windows = window_partition(shifted_y, self.window_size)  # nW*B, window_size, window_size, C
        y_windows = y_windows.view(-1, self.window_size * self.window_size, C)  # nW*B, window_size*window_size, C
        z_windows = window_partition(shifted_z, self.window_size)  # nW*B, window_size, window_size, C
        z_windows = z_windows.view(-1, self.window_size * self.window_size, C)  # nW*B, window_size*window_size, C

        # W-MSA/SW-MSA (to be compatible for testing on images whose shapes are the multiple of window size
        if self.input_resolution == x_size:
            x_attn_windows, y_attn_windows, z_attn_windows = self.attn(x_windows, y_windows, z_windows, mask=self.attn_mask)  # nW*B, window_size*window_size, C
        else:
            x_attn_windows, y_attn_windows, z_attn_windows = self.attn(x_windows, y_windows, z_windows, mask=self.calculate_mask(x_size).to(x.device))

        # merge windows
        x_attn_windows = x_attn_windows.view(-1, self.window_size, self.window_size, C)
        y_attn_windows = y_attn_windows.view(-1, self.window_size, self.window_size, C)
        z_attn_windows = z_attn_windows.view(-1, self.window_size, self.window_size, C)

        shifted_x = window_reverse(x_attn_windows, self.window_size, H, W)  # B H' W' C
        shifted_y = window_reverse(y_attn_windows, self.window_size, H, W)  # B H' W' C
        shifted_z = window_reverse(z_attn_windows, self.window_size, H, W)  # B H' W' C


        # reverse cyclic shift
        if self.shift_size > 0:
            x = torch.roll(shifted_x, shifts=(self.shift_size, self.shift_size), dims=(1, 2))
            y = torch.roll(shifted_y, shifts=(self.shift_size, self.shift_size), dims=(1, 2))
            z = torch.roll(shifted_z, shifts=(self.shift_size, self.shift_size), dims=(1, 2))
        else:
            x = shifted_x
            y = shifted_y
            z = shifted_z

        x = x.view(B, H * W, C)
        y = y.view(B, H * W, C)
        z = z.view(B, H * W, C)

        # FFN
        x = x_shortcut + self.drop_path(x)
        y = y_shortcut + self.drop_path(y)
        z = z_shortcut + self.drop_path(z)
        #print(x.size())
        x = x + self.drop_path(self.mlp_eoa(self.norm2_eoa(x)))
        y = y + self.drop_path(self.mlp_f(self.norm2_f(y)))
        z = z + self.drop_path(self.mlp_eaa(self.norm2_eaa(z)))

        return x, y, z